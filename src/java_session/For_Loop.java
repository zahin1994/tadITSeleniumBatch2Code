package java_session;

public class For_Loop {

	public static void main(String[] args) {
	

	 for (int a =5; a <= 100; a = a + 5 ) {
		System.out.print(a + " ");
		  }
	 System.out.println("  ");
	 
	 for (int i = 5; i + 1 < 99 ; i = i + 2) {
		 System.out.print(i + " ");
	 }
	 System.out.println("    ");
	 
	 for (int d = 2; d <= 98; d = d + 2) {
		 System.out.print(d + " ");
	 }
	 System.out.println("  ");

	 for (int b = 3; b <= 99 ; b = b + 3) { 
		 System.out.print(b + " ");
	 }
	 }
	 
	}
